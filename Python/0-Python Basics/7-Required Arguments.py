# Required Arguments of Python Functions
#
# 1. Number of arguments passed must be same as expected
# 2. The arguments must be in the same sequence
# --------------------------------------------------------

def myFunction(var1, var2):
    print("Var1 is : ", var1)
    print("Var2 is : ", var2)
    result = var1 + var2
    return result


a = 10
b = 20

res = myFunction(b, a)
print(res)


























